'''Ejercicio 1: Conversor de Temperatura
Escribe un programa que convierta una temperatura de grados Celsius a grados
Fahrenheit.'''

print ('Inserte grados Celsius')
grados_celsius = float(input())

def Conversor(grados_celsius):
        return (grados_celsius * 9/5) +32

print (f'La conversion a Farenheit es: ', Conversor (grados_celsius))







